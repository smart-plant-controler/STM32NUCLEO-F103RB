int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  __HAL_RCC_ADC_CONFIG(RCC_ADCPCLK2_DIV6); //PCLK2 / 6(≈12 MHz)
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  printf("\r\n[STM32] Soil moisture (ADC only) start\r\n");
  HAL_ADCEx_Calibration_Start(&hadc1); // F1 보정
  uint32_t t_sample = HAL_GetTick();
  uint32_t t_print  = HAL_GetTick();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  uint32_t now = HAL_GetTick();

	    // ① 주기적 샘플(10Hz): 이동평균
	    if ((uint32_t)(now - t_sample) >= SAMPLE_MS) {
	      t_sample += SAMPLE_MS;  // 누적형 갱신(드리프트 방지)
	      uint32_t a = adc_read_once();
	      if (!ema_init) { ema_init = 1; ema_adc = (float)a; }
	      else           { ema_adc = 0.8f*ema_adc + 0.2f*(float)a; } // 알파=0.2
	    }

	    // ② 주기적 출력(0.5s)
	    if ((uint32_t)(now - t_print) >= PRINT_MS) {
	      t_print += PRINT_MS;
	      uint32_t adc = (uint32_t)(ema_adc + 0.5f);
	      float volt = 3.3f * (float)adc / 4095.0f;
	      float pct  = moisture_percent(adc);
	      printf("ADC=%lu, V=%.3fV, Moisture=%.1f%%\r\n",
	             (unsigned long)adc, volt, pct);
	    }

	    // CPU 과점유 방지(선택)
	    HAL_Delay(1);
  }
  /* USER CODE END 3 */
}

