  HAL_TIM_Base_Start(&htim3);                          // TIM3 100 Hz
  HAL_ADC_Start_DMA(&hadc1,(uint32_t*)adc_buf,ADC_BUF_LEN);

  uint32_t t_print = HAL_GetTick();                    // 출력 타이밍
