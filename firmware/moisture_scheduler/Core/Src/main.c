	  if(adc_half){ adc_half=0; process_samples(&adc_buf[0],             ADC_BUF_LEN/2); }
	  if(adc_full){ adc_full=0; process_samples(&adc_buf[ADC_BUF_LEN/2], ADC_BUF_LEN/2); }

	  // 0.5초마다 상태 출력
	  if((uint32_t)(now - t_print) >= 500){
	    t_print += 500;
	    uint32_t adc = (uint32_t)(ema_adc + 0.5f);
	    float volt = 3.3f * (float)adc / 4095.0f;
	    float pct  = moisture_percent_u32(adc);
	    printf("ADC=%lu, V=%.3fV, Moisture=%.1f%%\r\n",(unsigned long)adc, volt, pct);
	  }

	  // 버튼 눌림(디바운스 후) 이벤트 (선택)
